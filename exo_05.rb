# #{} est pour faire une évaluation pour toutes opération ou comparaison et à afficher un résultat

puts "On va compter le nombre d'heures de travail à THP" # affiche le texte
puts "Travail : #{10 * 5 * 11}"  # affiche un texte avec le résultalt d'opération des heures de travail à THP
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"  # il en est de même pour cette ligne mais celle des minutes

puts "Et en secondes ?"# affiche un texte qui demande le nombre de seconde de travail

puts 10 * 5 * 11 * 60 * 60 # affiche le résultat du nombre de seconde de travail

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" # affiche un texte qui demande si cette comparaison est vrai

puts 3 + 2 < 5 - 7 # affiche la valeur de la comparaison

puts "Ça fait combien 3 + 2 ? #{3 + 2}" # affiche une question d'opération et le résultat
puts "Ça fait combien 5 - 7 ? #{5 - 7}" # idem que la ligne ci-dessus

puts "Ok, c'est faux alors !" # affiche une phase exlamatif 

puts "C'est drôle ça, faisons-en plus :" # affiche une phrase 

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" # affiche une question d'évaluation et un résultat VRAI
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"  # idem que la ligne ci-dessus
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"  # idem que la ligne ci-dessus mais avec un résltat FAUX